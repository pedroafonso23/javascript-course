// Estrutura de repeticao com teste no inicio: WHILE

var contador = 0
while (contador <= 5) {
    console.log(`Passo ${contador}`)
    contador++
}

// Estrutura de repeticao com teste no fim: DO WHILE

contador = 0
do {
    console.log(`Passo ${contador}`)
    contador++
} while (contador <= 55)

// Nos temos SEQUENCIAS, CONDICOES E REPETICOES (LOOPS)